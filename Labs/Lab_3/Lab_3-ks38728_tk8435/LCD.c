void DrawMinuteHand(uint32_t minute){
	if (minute > 59){
		ST7735_OutString("error"); // temp error catch
	}
 	ST7735_Line (OriginX,OriginY,Xcoor,Ycoor, BackgroundColor);

	Xcoor = XpolarToCoor(RADIUS, minuteToRadian(minute));
	Ycoor = YpolarToCoor(RADIUS, minuteToRadian(minute));
	
	ST7735_Line (OriginX,OriginY,Xcoor,Ycoor, ST7735_WHITE);
}

float minuteToRadian(uint32_t minute){
	float degree;
	float radians;
	
	degree = 90.0 - (float)minute*6.0;
	radians = ((degree)*(3.142857))/180.0;
	
	return (radians);
}

uint32_t XpolarToCoor(uint32_t radius, float theta){
	float tempX = radius*cos(theta);
	tempX += OriginX;
	return ((uint32_t)tempX);
}	

uint32_t YpolarToCoor(uint32_t radius, float theta){
	float tempY = radius*sin(theta);
	tempY = OriginY - tempY;
	return ((uint32_t)tempY);
}	

float hourToRadian(uint32_t hour, uint32_t minute){
	float degree;
	float radians;
	
	degree = (float)hour*30.0;
	degree = degree + 30.0 * (float)minute * 6.0 / 360.0;
	degree = 90.0 - degree;
	radians = degree * 3.142857 / 180.0;
	return (radians);
}

void DrawHourHand(uint32_t hour, uint32_t minute){	
	ST7735_Line (OriginX,OriginY,XcoorHour,YcoorHour, BackgroundColor);
	
	XcoorHour = XpolarToCoor(RADIUS - 10, hourToRadian(hour, minute));
	YcoorHour = YpolarToCoor(RADIUS - 10, hourToRadian(hour, minute));
	
	ST7735_Line (OriginX,OriginY,XcoorHour,YcoorHour, ST7735_WHITE);
}


void DrawDigitalDisplay(uint32_t hour, uint32_t minute){
	uint32_t isAM = 0;
	if (hour == 0){
		hour = 12;
		isAM = 1;
	}
	else if (hour < 12){
		isAM = 1;
	}
	else if (hour > 13){
		hour = hour - 12;
		isAM = 0;
	}
	ST7735_SetCursor(8,2);
	ST7735_OutUDec(hour/10);
	ST7735_OutUDec(hour%10);
	ST7735_OutChar(':');
	ST7735_OutUDec(minute/10);
	ST7735_OutUDec(minute%10);
	if (isAM == 1){
		ST7735_OutString(" AM");
	} else{
		ST7735_OutString(" PM");
	}
}

void DrawScreen(uint32_t hour,uint32_t  minute){
	switch(CurrState){
		case MAIN_SCREEN: 
			cursorCol = 0;
			cursorRow = 0;
			if(BtnPressed == NONE){ 
				DrawMainScreen(hour, minute); 
				}
			if(BtnPressed == SET){	
				CurrState = OPTIONS_SCREEN;
				cursorCol = 5;
				cursorRow = 4;
				DrawOptionScreen();
			}
			break;
		case OPTIONS_SCREEN:
			cursorCol = 5;
			switch(BtnPressed){
					case UP:
						cursorRow = cursorRow - 2;
						if (cursorRow < 4){
							cursorRow = 8;
						}
						DrawOptionScreen();
						break;
					case DOWN:
						cursorRow = cursorRow + 2;
						if (cursorRow > 8){
							cursorRow = 4;
						}
						DrawOptionScreen();
						break;
					case BACK:
						cursorCol = 0;
						cursorRow = 0;
						CurrState = MAIN_SCREEN;
						DrawMainScreenWithBkgnd(hour, minute);
						break;
					case SET: 
						if(cursorRow == 4){
							cursorCol = 5;
							cursorRow = 10;
							CurrState = SET_TIME_SCREEN;
							DrawSetScreen();
						}
						if(cursorRow == 6){
							cursorCol = 5;
							cursorRow = 10;
							CurrState = SET_ALARM_SCREEN;
							DrawSetScreen();
						}
						if(cursorRow == 8){
							cursorCol = 5;
							cursorRow = 5;
							CurrState = ON_OFF_ALARM_SCREEN;
							DrawOnOffAlarmScreen();
						}
						break;		
					}
			break; //options screen break
		case SET_TIME_SCREEN:
		case SET_ALARM_SCREEN:
			cursorRow = 10;
			switch(BtnPressed){
					case UP:
						if(cursorCol == 5){
							ChangeHour(screenHour, screenMinute, 1);
						}
						if(cursorCol == 10){
							ChangeMinute(screenHour, screenMinute, 10);
						}
						if(cursorCol == 15){
								ChangeMinute(screenHour, screenMinute, 1);
						}
						DrawSetScreen();
						break;
					case DOWN: 
						if(cursorCol == 5){
							ChangeHour(screenHour, screenMinute, -1);
						}
						if(cursorCol == 10){
							ChangeMinute(screenHour, screenMinute, -10);
						}
						if(cursorCol == 15){
								ChangeMinute(screenHour, screenMinute, -1);
						}
						DrawSetScreen();
						break;
					case BACK: 
						cursorCol = 5;
						cursorRow = 5;
						CurrState = OPTIONS_SCREEN;
						DrawOptionScreen();
						break;
					case SET:
						if (cursorCol >= 15){
								cursorRow = 5;
								cursorCol = 7;
								if (CurrState == SET_ALARM_SCREEN){
									alarmHour = screenHour;
									alarmMinute = screenMinute;
								} else {
									currentHour = screenHour;
									currentMinute = screenMinute;
								}
								CurrState = MAIN_SCREEN;
								DrawMainScreenWithBkgnd(currentHour, currentMinute);
						} else {
							cursorCol = cursorCol + 5;
							DrawSetScreen();
						} 
						break;
					}
			break;
		case ON_OFF_ALARM_SCREEN:
			cursorRow = 5;
			if (BtnPressed == UP || BtnPressed == DOWN){
				if (cursorCol == 5){
					cursorCol = 10;
				} else {
					cursorCol = 5;
				}
				void DrawOnOffAlarmScreen();
			}
			if (BtnPressed == SET){
				if(cursorCol == 5){
					alarmON = 1;
				} else {
					alarmON = 0;
				}
				CurrState = MAIN_SCREEN;
				DrawMainScreenWithBkgnd(hour, minute);
			}
			break;
		default:
			CurrState = MAIN_SCREEN;
			DrawMainScreenWithBkgnd(hour, minute);
	}
	BtnPressed = NONE;
}

void DrawMainScreen(uint32_t hour, uint32_t minute){
	DrawMinuteHand(minute);
	DrawHourHand(hour, minute);
	DrawDigitalDisplay(hour, minute);
}
	
void DrawMainScreenWithBkgnd(uint32_t hour, uint32_t minute){
	DrawClockBkgd();
	DrawMainScreen(hour, minute);
}

