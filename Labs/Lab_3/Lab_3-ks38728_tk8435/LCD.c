void ChangeHour(uint32_t hour, uint32_t minute, int32_t change){
	int temp = hour + change;
	if (temp < 0){
		temp = 12;
	}
	if (temp > 12){
		temp = 0;
	}
	screenHour = temp;
}

void ChangeMinute(uint32_t hour, uint32_t minute, int32_t change){
	int temp = minute + change;
	if (temp < 0){
		temp = 59;
	}
	if (temp > 59){
		temp = 0;
	}
	screenMinute = temp;
}

