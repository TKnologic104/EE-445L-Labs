/****************Name: ST7735_sDecOut3***************
 Author: Karime Saad, Tarang Khandpur
 Description: converts fixed point number to LCD
              format signed 32-bit with resolution 0.001
              range -9.999 to +9.999
 Inputs:  signed 32-bit integer part of fixed-point number
 Outputs: Prints exactly a 6 character fixed point number to LCD 
          Parameter LCD display
 12345    " *.***"
  2345    " 2.345"  digs[5,4,3,2]
 -8100    "-8.100"
  -102    "-0.102" 
    31    " 0.031" 
-12345    " *.***"
 */

void ST7735_sDecOut3(int32_t n){
	int digs[4] = {0,0,0,0};
	int count = 0;
	if ((n <= 9999) && (n >= -9999)) { //checks to see if number is in valid range
		if (n >= 0){
			ST7735_OutChar(' '); //outputs a space if input is positive
		}
		else {
			ST7735_OutChar('-'); //outputs a "-" if input is negative
			n = n * (-1); //converts the negative to positive
		}
		while (n > 0){
			digs[count] = n%10; //gets lowest value digit ex: 123, digs[0] = 3, digs[1] = 2, digs[2] = 1
			n = n / 10; //removes lowest value digit, ex: 123 -> 12, 12 -> 1, 1 -> 0
			count++;
		}
		count = 3; // 
		while (count >= 0){
			ST7735_OutChar(digs[count] + '0'); //+0 is to get the correct ascii code for a digit. This also prints the last number in the dig[].
			if (count == 3){
				ST7735_OutChar('.'); //prints the '.' after the first digit is output
			}
			count--;
		}
	}
	else {
		ST7735_OutChar(' ');
		ST7735_OutChar('*');
		ST7735_OutChar('.');
		ST7735_OutChar('*');
		ST7735_OutChar('*');
		ST7735_OutChar('*');
	}
}

/**************Name: ST7735_uBinOut8***************
 Author: Karime Saad, Tarang Khandpur 
Description: unsigned 32-bit binary fixed-point with a resolution of 1/256. 
             The full-scale range is from 0 to 999.99. 
             If the integer part is larger than 256000, it signifies an error. 
             The ST7735_uBinOut8 function takes an unsigned 32-bit integer part 
             of the binary fixed-point number and outputs the fixed-point value on the LCD
 Inputs:  unsigned 32-bit integer part of binary fixed-point number
 Outputs: Prints exactly a 6 character fixed point number to LCD 
Parameter LCD display
     0	  "  0.00"
     2	  "  0.01"
    64	  "  0.25"
   100	  "  0.39"
   500	  "  1.95"
   512	  "  2.00"
  5000	  " 19.53"
 30000	  "117.19"
255997	  "999.99"
256000	  "***.**"
*/

void ST7735_uBinOut8(uint32_t n){

	char outArray[6] = {'*','*','*','.','*','*'};
	int resolution = 8; //log(256)/log(2) ~~ log(resolution)/log(binary)
	uint32_t fractionalPart = 0;
	uint32_t intPart = 0;
	int count = 0;
	int bitValue = 0;

	if (n < 256000) {
		while (count < resolution){ //8 is used to isolate the rightmost 8 bits
			bitValue = (n & (1 << count));
			if (bitValue > 0){
				// in worst case we could have all bits as 1
				// then we need to add all the 1/2^m the position of that 1 to the right of binary decimal
				// thus there could be additions of 8 fractions
				// we could lose precision if we just consider 2 digits for each
				// so 1/ 2^1 = 0.5
				// so 1/ 2^2 = 0.25
				// so 1/ 2^3 = 0.125
				// so 1/ 2^4 = 0.0625
				// so 1/ 2^5 = 0.03125
				// so 1/ 2^6 = 0.015625
				// so 1/ 2^7 = 0.0078125
				// so 1/ 2^8 = 0.00390625
				// assume value is 1111 1111 which should be 255/256 = 0.99609 - correct answer should be 0.99
				// rounding - if we consider 2 digits to the right of decimal then sum is .5 + .25 + .12 + .06 + .03 + .01 = 1
				// rounding - if we consider 3 digits to the right of decimal then sum is .5 + .25 + .12 + .06 + .03 + .01 = .997
				// but integer division truncates
				// trunc - if we consider 2 digits to the right of decimal then sum is .5 + .25 + .12 + .06 + .03 + .01 = 0.97
				// trunc - if we consider 3 digits to the right of decimal then sum is .5 + .25 + .12 + .06 + .03 + .01 = .993
				
				// so we should multiply by 1009 to get 3 digits to the right of decimal and then drop off 1 digit
				fractionalPart = fractionalPart + (1000/(1 << (8 - count))); 
			}
			count++;
		}
// alt. 2 start
//	float	temp = 100.0 * n / 256 ; //multiply by 100 to get the 2 decimal digits. use .0 so that the results is in float with decimal precision
// 	fractionalPart = temp ; // will drop the unrequired digits after 5 digits.
// alt. 2 end

		fractionalPart = fractionalPart / 10; //drop the non-required digit
		outArray[5] = (fractionalPart % 10) + '0'; //get the least significant digit
		fractionalPart = fractionalPart / 10;
		outArray[4] = (fractionalPart % 10) + '0'; //get the next least significant digit
		outArray[3] = '.';
		intPart = (n >> 8);	// drop the last 2 digits which were part of the precision (right of decimal point)
		outArray[2] = (intPart % 10) + '0'; // get the unit digit
		intPart = intPart / 10;
		if (intPart > 0){
			outArray[1] = (intPart % 10) + '0'; //get the hundered digit
		}
		else {
			outArray[1] = ' ';
		}
		intPart = intPart / 10;
		if (intPart > 0){
			outArray[0] = (intPart % 10) + '0'; //get the thousand's digit
		}
		else {
			outArray[0] = ' ';
		}
	}
	ST7735_OutString(outArray);
}; 

