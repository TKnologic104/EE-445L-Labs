void ST7735_XYplot(uint32_t numPoints, int32_t bufX[], int32_t bufY[]){
	for (int i = 0; i < numPoints; i++){	
		ST7735_DrawPixel((10000 * bufX[i]/xScale + xOffset),(10000 * bufY[i]/yScale + yOffset),ST7735_BLUE);
		}
};

